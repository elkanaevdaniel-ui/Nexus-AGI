## LLM Cost Tiers
- Chat (Opus 4.6): Complex reasoning, market analysis — paid
- Utility (OpenRouter free): Simple tasks, formatting — FREE
- Embeddings (OpenAI): Memory search — cheapest
CODEBASE

cat > "$AGENT_ZERO_DIR/knowledge/custom/main/trading-strategy.md" << 'STRATEGY'
# Trading Strategy Knowledge

## How Edge Is Calculated
1. AI estimates the TRUE probability of an event (e.g., 70%)
2. Compare to the market price (e.g., 55 cents = 55% implied probability)
3. Edge = True probability - Market price - Fees
4. If edge > 3%, it's a trade opportunity

## Kelly Criterion (Position Sizing)
- Kelly fraction = (edge / odds) — tells you what % of bankroll to bet
- We use HALF Kelly (0.5x) — aggressive but not reckless
- Max 30% of bankroll on any single position
- Max 10 open positions at a time

## Risk Management
- Stop-loss at -30%: auto-sell if position drops 30% from entry
- Circuit breakers: auto-pause if daily loss exceeds threshold
- Correlation limits: don't overload on similar markets

## What Makes a Good Trade
- Clear, verifiable question with known resolution date
- Sufficient liquidity (>$500) so we can enter/exit
- Market price significantly different from our estimate (>3% edge)
- High confidence in our probability estimate
- NOT too close to resolution (prices become unpredictable near expiry)

## Common Pitfalls
- Overconfidence in AI estimates (they're often wrong)
- Trading illiquid markets (can't exit when needed)
- Correlated positions (multiple bets on same underlying event)
- Ignoring fees (2% taker fee eats into small edges)
STRATEGY

# ─── Step 5b: Deploy custom agent profile ───
echo "  Deploying custom agent profile..."
PROFILE_SRC="$POLYMARKET_DIR/agent-zero-profile"
PROFILE_DST="$AGENT_ZERO_DIR/agents/polymarket-trader"
PROMPTS_DIR="$AGENT_ZERO_DIR/prompts"
TOOLS_DIR="$AGENT_ZERO_DIR/python/tools"
if [ -d "$PROFILE_SRC" ]; then
    # Deploy to agent profile directory
    mkdir -p "$PROFILE_DST"
    cp "$PROFILE_SRC"/*.md "$PROFILE_DST/"
    echo "  Profile deployed to $PROFILE_DST"

    # ALSO deploy to prompts/ directory (belt and suspenders — works even without profile selection)
    echo "  Also deploying to prompts/ directory for default profile..."
    cp -f "$PROFILE_SRC"/agent.system.main.behaviour.md "$PROMPTS_DIR/"
    cp -f "$PROFILE_SRC"/agent.system.main.environment.md "$PROMPTS_DIR/"
    cp -f "$PROFILE_SRC"/agent.system.main.role.md "$PROMPTS_DIR/"
    cp -f "$PROFILE_SRC"/agent.system.tool.claude_code.md "$PROMPTS_DIR/"
    # Preserve original behaviour.md template but append our override
    if grep -q '{{rules}}' "$PROMPTS_DIR/agent.system.behaviour.md" 2>/dev/null; then
        cp -f "$PROFILE_SRC"/agent.system.behaviour.md "$PROMPTS_DIR/"
        echo "  Patched agent.system.behaviour.md with override directive"
    else
        cp -f "$PROFILE_SRC"/agent.system.behaviour.md "$PROMPTS_DIR/"
    fi
    echo "  Prompt files deployed to $PROMPTS_DIR"

    # Deploy Claude Code CLI tool
    if [ -f "$PROFILE_SRC/claude_code_cli.py" ] && [ -d "$TOOLS_DIR" ]; then
        cp -f "$PROFILE_SRC/claude_code_cli.py" "$TOOLS_DIR/"
        echo "  Claude Code CLI tool deployed to $TOOLS_DIR"
    elif [ -f "$PROFILE_SRC/claude_code_cli.py" ]; then
        mkdir -p "$TOOLS_DIR"
        cp -f "$PROFILE_SRC/claude_code_cli.py" "$TOOLS_DIR/"
        echo "  Claude Code CLI tool deployed to $TOOLS_DIR (created dir)"
    fi
else
    echo "  WARNING: Profile source not found at $PROFILE_SRC"
fi

# ─── Step 6: Create Claude Code wrapper ───
echo "[6/8] Creating Claude Code wrapper script..."
cat > "$HOME/claude-trade.sh" << 'WRAPPER'
#!/usr/bin/env bash
# Claude Code wrapper for Agent Zero integration
export ANTHROPIC_API_KEY=$(grep "^ANTHROPIC_API_KEY=" ~/polymarket-agent/.env | cut -d= -f2- | tr -d '"')
cd ~/polymarket-agent
claude -p "$@"
WRAPPER
chmod +x "$HOME/claude-trade.sh"

# ─── Step 7: Generate service token ───
echo "[7/8] Generating API service token..."
TOKEN=$(curl -s -X POST http://localhost:8000/auth/token 2>/dev/null | python3 -c "import sys,json; print(json.load(sys.stdin).get('access_token',''))" 2>/dev/null || echo "")
if [ -n "$TOKEN" ]; then
    echo "$TOKEN" > "$HOME/.polymarket-token"
    chmod 600 "$HOME/.polymarket-token"
    echo "  Token saved to ~/.polymarket-token"
else
    echo "  WARNING: Could not generate token (bot may not be running yet)"
    echo "  Run this after bot starts: curl -s -X POST http://localhost:8000/auth/token | python3 -c \"import sys,json; print(json.load(sys.stdin)['access_token'])\" > ~/.polymarket-token"
fi

# ─── Step 8: Create systemd service ───
echo "[8/8] Creating systemd service..."
sudo tee /etc/systemd/system/agent-zero.service > /dev/null << SERVICEEOF
[Unit]
Description=Agent Zero AI Assistant
After=network.target polymarket-agent.service
Wants=polymarket-agent.service

[Service]
Type=simple
User=$(whoami)
WorkingDirectory=$AGENT_ZERO_DIR
Environment=PATH=$AGENT_ZERO_DIR/venv/bin:/usr/local/bin:/usr/bin:/bin
ExecStart=$AGENT_ZERO_DIR/venv/bin/python run_ui.py
Restart=on-failure
RestartSec=10

[Install]
WantedBy=multi-user.target
SERVICEEOF

sudo systemctl daemon-reload
sudo systemctl enable agent-zero
echo "  Service created (not started yet)"

echo ""
echo "=== Setup Complete ==="
echo "Start Agent Zero:   sudo systemctl start agent-zero"
echo "Web UI:             http://localhost:50001"
echo "Check status:       sudo systemctl status agent-zero"
echo ""
echo "IMPORTANT: If the Polymarket bot wasn't running during setup,"
echo "regenerate the token after starting it:"
echo "  curl -s -X POST http://localhost:8000/auth/token | python3 -c \"import sys,json; print(json.load(sys.stdin)['access_token'])\" > ~/.polymarket-token"
